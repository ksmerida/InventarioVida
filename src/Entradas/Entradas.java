    private void sedes() {
        try {
            CallableStatement cs = Cn.prepareCall("{CALL sp_entradas(?)}");
            cs.setString(1, "LS"); // opción listar sedes
            ResultSet rs = cs.executeQuery();

            cmbSede.removeAllItems();
            while (rs.next()) {
                cmbSede.addItem(rs.getInt("id_sede") + " - " + rs.getString("nombre_sede"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar sedes: " + e.getMessage());
        }
    }

    private void loadBodegasSede(int sedeId) {
        try {
            CallableStatement cs = Cn.prepareCall("{CALL sp_entradas(?, ?)}");
            cs.setString(1, "LB"); // opción para listar bodegas
            cs.setInt(2, sedeId);  // id de la sede
            ResultSet rs = cs.executeQuery();

            cmbBodega.removeAllItems();

            while (rs.next()) {
                cmbBodega.addItem(rs.getString("nombre_bodega")); // o usar ComboItem si quieres el id
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar bodegas: " + e.getMessage());
        }
    }
    
