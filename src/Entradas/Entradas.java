    private void cmbSedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbSedeActionPerformed
        if (cmbSede.getSelectedIndex() > -1) { // Evita errores si no hay selección
            String selected = (String) cmbSede.getSelectedItem();
            if (selected != null && !selected.equals("")) {
                String[] parts = selected.split(" - ");
                try {
                    int sedeId = Integer.parseInt(parts[0].trim());
                    loadBodegasSede(sedeId);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Error al obtener el ID de la sede.");
                }
            }
        }
    }//GEN-LAST:event_cmbSedeActionPerformed


